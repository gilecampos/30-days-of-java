package day02_operators;

public class RelationalOperators {
    public static void main(String[] args) {
        int x = 20;
        int y = 15;

        System.out.println(x > y);
        System.out.println(x >= y);
        System.out.println(x < y);
        System.out.println(x == y);
        System.out.println(x != y);
    }
}
